# ==================== 消息分割 / 正则保护 ====================

# 参与正则分割的文本长度上限。超过该长度则跳过基于正则的分割、整条发送，
# 以缓解管理员配置的复杂正则在长文本上发生灾难性回溯（ReDoS）的风险。
# 主动消息为有限的 LLM 输出，正常情况下远小于该阈值。
MAX_SPLIT_TEXT_LENGTH = 10000

# 管理员配置的用户正则长度上限，超过则记录复杂度提示日志（不阻断）。
MAX_USER_REGEX_LENGTH = 200

# 容易引发灾难性回溯的正则结构（嵌套量词等）启发式特征，仅用于日志提示。
REDOS_RISK_PATTERN_HINTS = (
    r"\([^)]*[+*]\)[+*]",  # 形如 (a+)+ / (a*)* 的嵌套量词
    r"\([^)]*[+*]\)\{",  # 形如 (a+){n,} 的嵌套量词
)


